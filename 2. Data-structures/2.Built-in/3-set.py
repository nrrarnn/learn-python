# set

# Set is one of the built-in data structures in Python which is used to store unique and unordered collections of items. Sets are similar to the concept of sets in mathematics.

# Set Characteristics
# Unique: The set does not contain duplicate elements.
# Unordered: The elements in a set are unordered and have no index.
# Mutable: You can add or remove elements from a set, but individual elements in the set must be immutable (such as numbers, strings, or tuples).

# example
