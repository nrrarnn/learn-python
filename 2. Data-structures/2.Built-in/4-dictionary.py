# Dictionary

# Dictionary is one of the most important and frequently used built-in data structures in Python. Dictionaries store data in the form of key-value pairs, where each key is unique and serves as an index for the corresponding value.

# using curly braces
my_dict = {'key1' : 'value1', 'key2':'value2'}

# using function
my_dict2 = dict(age=18,height=160,weight=47)

print(my_dict2)

# access value
print(my_dict['key1'])  #value1

# add
my_dict['key3'] = 'value3'
print(my_dict)

