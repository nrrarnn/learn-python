#  For Loop

# The for loop in Python is used to iterate over a sequence (such as a list, tuple, dictionary, set, or string) or other iterable objects. The loop iterates over each item in the sequence, executing the block of code within the loop for each item.

# Basic Syntax

# for variable in sequence:
    # Code block to be executed for each item in the sequence


fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

#  Output :   
# apple
# banana
# cherry

# Using Range

for i in range(5):
    print(i)

# Output
# 0
# 1
# 2
# 3
# 4
