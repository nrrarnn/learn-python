# Integer(int) 
# Whole numbers, both positive and negative, without decimals.
a = 10
b = 20
print(a + b)  # Output: 30
print(a - b)  # Output : -10
print(a * b)  # Output : 200
print(a / b)  # Output : 0.5
print(a % b)  # Output : 10
print(a ** b) # Output : 100000000000000000000
print(a // b) # Output : 0
# Operations: Add (+), subtract (-), multiply (*), divide (/), modulus (%), exponent (**), integer division (//).
