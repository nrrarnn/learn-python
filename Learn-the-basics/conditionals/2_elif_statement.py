# elif Statement:

# Stands for "else if". It allows you to check multiple expressions for true and execute a block of code as soon as one of the conditions is true. It follows an if statement and comes before an else statement.

x = 10
if x > 15:
    print("x is greater than 15")
elif x > 5:
    print("x is greater than 5 but less than or equal to 15")