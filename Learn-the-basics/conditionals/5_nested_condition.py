# Nested Conditionals:
# You can nest conditional statements within other conditional statements to create more complex decision-making processes.

x = 10
y = 20

if x > 5:
    if y > 15:
        print("x is greater than 5 and y is greater than 15")
    else:
        print("x is greater than 5 but y is not greater than 15")
else:
    print("x is not greater than 5")
